# 4. Bitwise Operators (&,|,^,~,<<,>>)
# & operator
print(5 & 3) # 101 & 011 = if both are 1 then 1 else 0 = 001
# | operator
print(5 | 3) # 101 | 011 = if any of one then 1 else 0 = 111
# ^ operator
print(5 ^ 3) # 101 ^ 011 = if both are different then 1 else 0 = 110
# ~ operator
print(~5) # ~x = -(x + 1) = -6 
# >> operator
print(5 >> 2) # 101 => 001
# << operator
print(5 << 2) # 101 => 100

