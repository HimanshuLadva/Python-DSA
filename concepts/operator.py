# 1. Arithmetic Operators (+,-,*,/,//,%,**)
# // operator
print(7 / 2) # 3.5
print(7 // 2) # 3
print(-7 // 2) # 4
# % operator
print(10 % 3) # 1
print(15 % 4) # 3
print(20 % 5) # 0
# ** operator
print(2 ** 3) # 2*2*2 = 8
print(5 ** 2) # 5*5 = 25

